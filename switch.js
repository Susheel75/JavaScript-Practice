var grade=30;
switch(true){
    case grade>40:
        console.log("A")
        break;
    case grade>30:
        console.log("B")
        break;
    case grade>20:
        console.log("C")
        break;
    case grade>10:
        console.log("D")
        break;
    case grade>0:
        console.log("F")
        break;
    default:
        console.log("Invalid grade")
}